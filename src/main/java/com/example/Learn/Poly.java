package com.example.Learn;

public class Poly {
    // overloading
    public void Ov(int a,int b){
        System.out.println(a + b);
    }
    public void Ov(int a,int b, int c){
        System.out.println(a + b + c);
    }
    public void Ov(int a,int b, int c, int d){
        System.out.println(a + b + c);
    }
}
